package com.inter.entity;

public class InterfaceRecord {
	private int impId;
	private String impName;
	private String environment;
	private String requestStyle;
	private String dataStyle;
	private String requestURL;
	public int getImpId() {
		return impId;
	}
	public void setImpId(int impId) {
		this.impId = impId;
	}
	public String getImpName() {
		return impName;
	}
	public void setImpName(String impName) {
		this.impName = impName;
	}
	public String getEnvironment() {
		return environment;
	}
	public void setEnvironment(String environment) {
		this.environment = environment;
	}
	public String getRequestStyle() {
		return requestStyle;
	}
	public void setRequestStyle(String requestStyle) {
		this.requestStyle = requestStyle;
	}
	public String getDataStyle() {
		return dataStyle;
	}
	public void setDataStyle(String dataStyle) {
		this.dataStyle = dataStyle;
	}
	public String getRequestURL() {
		return requestURL;
	}
	public void setRequestURL(String requestURL) {
		this.requestURL = requestURL;
	}
	

}
